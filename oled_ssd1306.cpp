Adafruit_SSD1306 oled(OLED_RESET);
/**
 * @brief Oled 初始化，并让它显示初始的logo
 * 
 */
void OledInit()
{
    oled.begin(SSD1306_SWITCHCAPVCC,0x3C);
    oled.clearDisplay();

    //开始卷王号Logo
    Oled_PrintLogo(true);
    //delay(1000);
}

/**
 * @brief 打印Logo到屏幕上  
 * 
 * @param clear 是否清除已有屏幕输出
 */
void Oled_PrintLogo(bool clear)
{
    if(clear)
        oled.clearDisplay();
    oled.drawBitmap(4, -5, Text_juan1, 40, 40, 1); //在坐标X:26  Y:16的位置显示中文字符凌
    oled.drawBitmap(46, -5, Text_juan2, 40, 40, 1); //在坐标X:42  Y:16的位置显示中文字符顺
    oled.drawBitmap(88, -5, Text_juan3, 40, 40, 1);
    oled.display();
}

/**
 * @brief 输出字符串到屏幕上（仅限非中文字符）
 * 
 * @param textSize 字体大小
 * @param isWhite 字体颜色（true为白色，反之白底黑字）
 * @param x 初始位置
 * @param y 初始位置
 * @param str 输入的字符串
 * @param displayNow 是否立刻输出
 * @param clearNow 写入前是否需要清除屏幕上已有图形和文字
 */
void Oled_PrintChar(int textSize,bool isWhite,int x,int y,String str,bool displayNow,bool clearNow)
{
    if(clearNow)
        oled.clearDisplay();

    oled.setTextSize(textSize);
    if(isWhite)
        oled.setTextColor(WHITE);
    else
        oled.setTextColor(BLACK,WHITE);
    
    oled.setCursor(x,y);
    oled.println(str);
    if(displayNow)
        oled.display();
    
}

/**
 * @brief 上一个函数的重载，主要用于输出数字
 * 
 * @param textSize 
 * @param isWhite 
 * @param x 
 * @param y 
 * @param intNum 要输出的整形数字
 * @param displayNow 
 * @param clearNow 
 */
void Oled_PrintChar(int textSize,bool isWhite,int x,int y,int intNum,bool displayNow,bool clearNow)
{
    if(clearNow)
        oled.clearDisplay();
        //oled.clear();

    oled.setTextSize(textSize);
    if(isWhite)
        oled.setTextColor(WHITE);
    else
        oled.setTextColor(BLACK,WHITE);
    
    oled.setCursor(x,y);
    oled.println(intNum);
    if(displayNow)
        oled.display();
    
}
